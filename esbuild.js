const esbuild = require("esbuild");
const fs = require("fs");
const path = require("path");

const production = process.argv.includes('--production');
const watch = process.argv.includes('--watch');

/**
 * @type {import('esbuild').Plugin}
 */
const esbuildProblemMatcherPlugin = {
	name: 'esbuild-problem-matcher',

	setup(build) {
		build.onStart(() => {
			console.log('[watch] build started');
		});
		build.onEnd((result) => {
			result.errors.forEach(({ text, location }) => {
				console.error(`âœ˜ [ERROR] ${text}`);
				console.error(`    ${location.file}:${location.line}:${location.column}:`);
			});
			console.log('[watch] build finished');
		});
	},
};

/**
 * @type {import('esbuild').Plugin}
 */
const copyEmojiDataPlugin = {
	name: 'copy-emoji-data',

	setup(build) {
		build.onEnd(() => {
			// Copy emoji-data.jsonl to dist/src/
			const srcPath = path.join(__dirname, 'src', 'emoji-data.jsonl');
			const distDir = path.join(__dirname, 'dist', 'src');
			const distPath = path.join(distDir, 'emoji-data.jsonl');

			// Create dist/src directory if it doesn't exist
			if (!fs.existsSync(distDir)) {
				fs.mkdirSync(distDir, { recursive: true });
			}

			// Copy the file
			fs.copyFileSync(srcPath, distPath);
		});
	},
};

async function main() {
	const ctx = await esbuild.context({
		entryPoints: [
			'src/extension.ts'
		],
		bundle: true,
		format: 'cjs',
		minify: production,
		sourcemap: !production,
		sourcesContent: false,
		platform: 'node',
		outfile: 'dist/extension.js',
		external: ['vscode'],
		logLevel: 'silent',
		plugins: [
			copyEmojiDataPlugin,
			/* add to the end of plugins array */
			esbuildProblemMatcherPlugin,
		],
	});
	if (watch) {
		await ctx.watch();
	} else {
		await ctx.rebuild();
		await ctx.dispose();
	}
}

main().catch(e => {
	console.error(e);
	process.exit(1);
});
